    public void populateJtable()
    {
        ArrayList<Clases.CategoryClass> catList = catclass.catList();
        
        String[] colNames = {"Category ID", "Name"};
        
        Object[][] rows = new Object[catList.size()][colNames.length];
        
        for(int i = 0; i < catList.size(); i++)
        {
            rows[i][0] = catList.get(i).getId();
            rows[i][1] = catList.get(i).getName();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_category.setModel(model);
        
    }
    
    
    public void selectRow()
    {
        int index = jTable_category.getSelectedRow();
        
        String id = jTable_category.getValueAt(index, 0).toString();
        String name = jTable_category.getValueAt(index, 1).toString();
        
        jLabel_id.setText(id);
        txt_name.setText(name);
        
        jButton_add.setEnabled(false);
        
    }
    
    
    public void add()
    {
        String name = txt_name.getText();
        
        if(name.isEmpty())
        {
            empty_error.setVisible(true);
        }
        else
        {
            catclass.addCategory(name);
        }
        
        txt_name.setText("");
        
    }
    
    public void update()
    {
        try{
            int id = Integer.parseInt(jLabel_id.getText().trim());
            String name = txt_name.getText();

            if(name.isEmpty())
            {
                empty_error.setVisible(true);
            }
            else
            {
                catclass.updateCategory(id, name);
            }

            jLabel_id.setText("");
            txt_name.setText("");
            jButton_add.setEnabled(true);
            
        } catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please select Category" , "Category", 1);
        }    
        
    }
    
    public void delete()
    {
        try{
            
            int id = Integer.parseInt((jLabel_id.getText().trim()));

            catclass.delCategory(id);

            jLabel_id.setText("");
            txt_name.setText("");
            jButton_add.setEnabled(true);
            
        } catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please select Category" , "Category", 1);
        }
    }
    

